mes = parseInt(prompt("digite um número"));

switch (mes) {
    case 1:
        window.alert("janeiro")
        break;
    case 2:
        window.alert("fevereiro")
        break;
    case 3:
        window.alert("março")
        break;
    case 4:
        window.alert("abril")
        break;
    case 5:
        window.alert("maio")
        break;
    case 6:
        window.alert("junho")
        break;
    case 7:
        window.alert("julho")
        break;
    case 8:
        window.alert("agosto")
        break;
    case 9:
        window.alert("setembro")
        break;
    case 10:
        window.alert("outubro")
        break;
    case 11:
        window.alert("novembro")
        break;
    case 12:
        window.alert("dezembro")
        break;
    default:
        window.alert("mês inválido")
        window.location.reload
        break;
}